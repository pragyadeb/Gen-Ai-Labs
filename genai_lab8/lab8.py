z1 = tf.random.normal([1, latent_dim])
z2 = tf.random.normal([1, latent_dim])

alphas = np.linspace(0,1,10)

images = []

for a in alphas:
    z = (1-a)*z1 + a*z2
    img = generator(z, training=False)
    images.append(img[0])

images = (np.array(images)+1)/2

